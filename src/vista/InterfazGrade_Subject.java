    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        LinkedList<grade> listac = null;
        LinkedList<subject> listag = null;
        LinkedList<teacher> listat = null;

        ControllerGrade objcc = new ControllerGrade();
        ControllerSubject objgj = new ControllerSubject();
        ControllerTeacher objce = new ControllerTeacher();
        
        listac = objcc.insertGrade();
        for (int i = 0; i < listac.size(); i++) {
            grade grade = listac.get(i);
            jComboBox1.addItem(grade.getNameGr());
        }

        listag = objgj.insertSubject();
        for (int i = 0; i < listag.size(); i++) {
            subject subject = listag.get(i);    
            jComboBox2.addItem(subject.getNameSu());

        }       
        
        listat = objce.insertTeacher();
        for (int i = 0; i < listat.size(); i++) {
            teacher teacher = listat.get(i);
        jComboBox3.addItem(teacher.getNameT1() + " " + teacher.getLastNameT1());

        }     
    }//GEN-LAST:event_formWindowOpened

